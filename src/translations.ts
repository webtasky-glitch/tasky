export function useTranslation() {
  const { language } = useTasky() as any;
  
  const t = (key: keyof typeof translationDict.en | keyof typeof translationDict.el): string => {
    const lang = (language || 'en') as Language;
    return translationDict[lang][key] || translationDict['en'][key] || String(key);
  };
  
  return { t, language };
}
